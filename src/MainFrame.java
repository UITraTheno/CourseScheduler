    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String SEMESTER = currentSemester;
        String COURSECODE = DropCS.getSelectedItem().toString();
        String output = null;
        ArrayList<ScheduleEntry> SCHlist = ScheduleQueries.getScheduledStudentByCourse(SEMESTER,COURSECODE);        
        output = "Dropping students who scheduled:\n";
        for (ScheduleEntry schedule:SCHlist){
            String studentid = schedule.getStudentID();
            StudentEntry student1 = StudentQueries.getStudent(studentid);
            String FIRSTNAME1 = student1.getFirstName();
            String LASTNAME1 = student1.getLastName();
            output = output + FIRSTNAME1 + "," + LASTNAME1 + " " + studentid + " has been dropped from " + COURSECODE + ".\n";
        }
        output = output + "\nDropping students who waitlisted:\n";
        ArrayList<ScheduleEntry> wailist = ScheduleQueries.getWaitlistedStudentByCourse(SEMESTER,COURSECODE);
        for (ScheduleEntry waitlist:wailist){
            String STUDENTID = waitlist.getStudentID();
            StudentEntry student2 = StudentQueries.getStudent(STUDENTID);
            String FIRSTNAME2 = student2.getFirstName();
            String LASTNAME2 = student2.getLastName();
            output = output + FIRSTNAME2 + "," + LASTNAME2 + " " + STUDENTID + " has been dropped from " + COURSECODE + ".\n";
        }
        DropCourseS.setText(output);
        CourseQueries.dropCourse(SEMESTER, COURSECODE);
        ScheduleQueries.DropSchByCourse(SEMESTER, COURSECODE);
        rebuildCourseComboBoxes();// TODO add your handling code here:
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

