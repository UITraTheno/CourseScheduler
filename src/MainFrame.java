    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        ArrayList<String> SEMESTER = SemesterQueries.getSemesterList();
        ArrayList<StudentEntry>student = StudentQueries.getAllStudent();
        String STUDENTID = student.get(DropStudentCom.getSelectedIndex()).getStudentID();
        String STUDENTINFOR = DropStudentCom.getSelectedItem().toString();
        String outCourse = null;
        outCourse = STUDENTINFOR + " Has been dropped from the list of students\n";
        for (String semester:SEMESTER){
            outCourse = outCourse + "\n" + "For Semester:" + semester + "\n";
            ArrayList<ScheduleEntry> COURSES = ScheduleQueries.getScheduleByStudent(semester, STUDENTID);
            for (ScheduleEntry course:COURSES){
                String coursecode = course.getCourseCode();
                outCourse = outCourse + STUDENTINFOR + " has been dropped from " + coursecode + "\n";
                ScheduleQueries.updateScheduleEntry(semester,course);
            }
        }
        DropStuText.setText(outCourse);
        StudentQueries.dropStudent(STUDENTID);
        ScheduleQueries.DropSchByStudent(STUDENTID);
        rebuildStudentComboBoxes();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

